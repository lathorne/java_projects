public class pixel {

    int red;
    int green;
    int blue;

    pixel(int r, int g, int b){
        red = r;
        green = g;
        blue = b;
    }

    int getRed(){
    	return red;
    }

    int getGreen(){
    	return green;
    }

    int getBlue(){
    	return blue;
    }

    void setRed(int r){
    	red = r;
    }

    void setGreen(int g){
    	green = g;
    }

    void setBlue(int b){
    	blue = b;
    }

}
